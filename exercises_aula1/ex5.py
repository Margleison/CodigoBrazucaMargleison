#Mostre na tela o dobro, a raiz quadrada e o triplo de um numero
print("Insira dois numeros de sua escolha e verá o dobro, a raiz quadrada e o triplo.")


x = int(input("numero 1: "))

y = int(input("numero 2: "))

print(f" O dobro é=  {x*y}")


print(f" A raiz quadrada é=  {x**y}")


print(f"O seu triplo é= {(x+y)*3} ")